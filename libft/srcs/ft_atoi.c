#include "../includes/libft.h"

int	ft_atoi(const char *str)
{
	char	*s;
	int		r;
	int		sign;

	s = (char *)str;
	sign = 1;
	r = 0;
	while ((9 <= *s && *s <= 13) || *s == 32)
		s++;
	if (ft_memcmp(s, "-2147483648", 11) == 0)
		return (-2147483648);
	if (*(s) == '+' || *s == '-')
	{
		if (*(s++) == '-')
			sign *= -1;
	}
	s--;
	while (ft_isdigit(*(++s)))
		r = r * 10 + *s - '0';
	return (r * sign);
}
